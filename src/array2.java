public class array2 {
    public static void main(String[] args) {
        int [] marks = {43,76,97,98};
        System.out.println(marks.length);  //for length of array--- array_name.length----->no parenthesis after/before

        // displaying array using for-each loop
        for(int j: marks){
            System.out.println(j);
        }
    }
}
